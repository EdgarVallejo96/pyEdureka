# Exception Handling
# Important terms
# try: keyword used to keep the code segment under check
# except: segment to handle the exception after catching it
# else: run this when no exceptions exist
# finally: no matter what, run this code even if / if not has an exception

# The keyword 'raise': Let's you throw an exception if a condition occurs
print('raise keyword')
x = 10
if x > 5:
    raise Exception('x should not exceed 5. The value of x was: {}'.format(x))

